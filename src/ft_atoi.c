#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sum;
	int	i;
	int	neg;

	i = 0;
	sum = 0;
	neg = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		sum = sum * 10 + (str[i] - 48);
		i++;
	}
	return (sum * neg);
}
